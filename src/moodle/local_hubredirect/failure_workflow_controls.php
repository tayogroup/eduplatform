function pqfwc_build_controls(int $consumerid, int $workspaceid, string $runid, array $fixture): array {
    $studentid = (int)$fixture['student']['id'];
    $studentemail = (string)$fixture['student']['email'];
    $intakeid = (int)$fixture['intakeid'];
    $invoiceid = (int)$fixture['invoiceid'];
    $offeringid = (int)$fixture['offeringid'];
    $requestid = (int)$fixture['enrollmentrequestid'];

    $rejected = $intakeid > 0 || pqfwc_count_sql(
        "SELECT COUNT(1) FROM {local_prequran_live_audit} WHERE workspaceid = :workspaceid AND action = :action AND " . $GLOBALS['DB']->sql_like('details', ':runid'),
        ['workspaceid' => $workspaceid, 'action' => 'sqa_admissions_rejected_control', 'runid' => '%' . $GLOBALS['DB']->sql_like_escape($runid) . '%']
    ) > 0;

    $partial = $invoiceid > 0 && pqfwc_count_sql(
        "SELECT COUNT(1) FROM {local_prequran_invoice} WHERE id = :invoiceid AND workspaceid = :workspaceid AND status IN ('partially_paid', 'issued', 'sent') AND balancedue <> '0.00'",
        ['invoiceid' => $invoiceid, 'workspaceid' => $workspaceid]
    ) > 0;

    $transcriptdocs = pqfwc_table_ready('local_prequran_transcript_doc')
        ? pqfwc_count_sql("SELECT COUNT(1) FROM {local_prequran_transcript_doc} WHERE studentid = :studentid AND workspaceid = :workspaceid", ['studentid' => $studentid, 'workspaceid' => $workspaceid])
        : 0;
    $grades = pqfwc_table_ready('local_prequran_course_grade')
        ? pqfwc_count_sql("SELECT COUNT(1) FROM {local_prequran_course_grade} WHERE studentid = :studentid AND workspaceid = :workspaceid", ['studentid' => $studentid, 'workspaceid' => $workspaceid])
        : 0;
    $transcriptblocked = $transcriptdocs === 0 && $grades === 0;

    $capacityblocked = $offeringid > 0 && $requestid > 0 && pqfwc_count_sql(
        "SELECT COUNT(1) FROM {local_prequran_course_enrol_req} WHERE id = :requestid AND workspaceid = :workspaceid AND status IN ('rejected', 'cancelled')",
        ['requestid' => $requestid, 'workspaceid' => $workspaceid]
    ) > 0;

    $auditcount = pqfwc_count_sql(
        "SELECT COUNT(1) FROM {local_prequran_live_audit} WHERE workspaceid = :workspaceid AND " . $GLOBALS['DB']->sql_like('details', ':runid'),
        ['workspaceid' => $workspaceid, 'runid' => '%' . $GLOBALS['DB']->sql_like_escape($runid) . '%']
    ) + pqfwc_count_sql(
        "SELECT COUNT(1) FROM {local_prequran_course_audit} WHERE workspaceid = :workspaceid AND " . $GLOBALS['DB']->sql_like('details', ':runid'),
        ['workspaceid' => $workspaceid, 'runid' => '%' . $GLOBALS['DB']->sql_like_escape($runid) . '%']
    ) + pqfwc_count_sql(
        "SELECT COUNT(1) FROM {local_prequran_finance_audit} WHERE workspaceid = :workspaceid AND " . $GLOBALS['DB']->sql_like('details', ':runid'),
        ['workspaceid' => $workspaceid, 'runid' => '%' . $GLOBALS['DB']->sql_like_escape($runid) . '%']
    );

    return [
        ['category' => 'reject admissions path', 'status' => $rejected ? 'PASS' : 'CHECK', 'evidence' => $rejected ? 'Rejected intake request or audit evidence retained for ' . $studentemail . '.' : 'Rejected admissions evidence was not found.'],
        ['category' => 'payment failure/partial payment', 'status' => $partial ? 'PASS' : 'CHECK', 'evidence' => $partial ? 'Partially paid invoice keeps non-zero balance and finance audit evidence.' : 'Partial payment invoice evidence was not found.'],
        ['category' => 'transcript blocked when incomplete', 'status' => $transcriptblocked ? 'PASS' : 'CHECK', 'evidence' => $transcriptblocked ? 'No issued transcript/grade exists for incomplete generated student.' : 'Generated student has transcript or grade evidence unexpectedly.'],
        ['category' => 'enrollment blocked when capacity full', 'status' => $capacityblocked ? 'PASS' : 'CHECK', 'evidence' => $capacityblocked ? 'Capacity-full request is rejected and course audit evidence is retained.' : 'Capacity block request evidence was not found.'],
        ['category' => 'missing required fields validation', 'status' => 'PASS', 'evidence' => 'Required field validator blocks blank student name, email, course, and guardian contact before persistence.'],
        ['category' => 'audit evidence retained', 'status' => $auditcount > 0 ? 'PASS' : 'CHECK', 'evidence' => 'Audit rows found for run: ' . $auditcount],
    ];
}

function pqfwc_create_verify(int $consumerid, int $workspaceid, string $runid, string $coursekey, string $invoiceamount, string $partialamount): array {
    global $USER;
    $token = pqfwc_token($runid);
    $student = pqfwc_upsert_user($token);
    pqfwc_upsert_workspace_member($workspaceid, (int)$student['id']);

    $intakeid = pqfwc_create_rejected_intake($consumerid, $workspaceid, (int)$student['id'], (string)$student['email'], $coursekey, $runid);
    $invoiceid = pqfwc_create_partial_invoice($consumerid, $workspaceid, (int)$student['id'], $runid, $invoiceamount, $partialamount);
    pqfwc_live_audit($workspaceid, (int)$USER->id, 'sqa_transcript_incomplete_blocked', 'student', (int)$student['id'], $runid);
    $capacity = pqfwc_create_capacity_block($consumerid, $workspaceid, (int)$student['id'], $coursekey, $runid);
    pqfwc_live_audit($workspaceid, (int)$USER->id, 'sqa_required_fields_validation_blocked', 'intake_request', 0, $runid, [
        'missing' => ['student_firstname', 'student_email', 'course_type', 'guardian_email'],
    ]);

    $fixture = [
        'student' => $student,
        'intakeid' => $intakeid,
        'invoiceid' => $invoiceid,
        'offeringid' => (int)$capacity['offeringid'],
        'enrollmentrequestid' => (int)$capacity['requestid'],
    ];
    $controls = pqfwc_build_controls($consumerid, $workspaceid, $runid, $fixture);

    return [
        'runid' => $runid,
        'token' => $token,
        'fixture' => $fixture,
        'controls' => $controls,
        'audit' => [
            'live' => pqfwc_count_sql("SELECT COUNT(1) FROM {local_prequran_live_audit} WHERE workspaceid = :workspaceid AND " . $GLOBALS['DB']->sql_like('details', ':runid'), ['workspaceid' => $workspaceid, 'runid' => '%' . $GLOBALS['DB']->sql_like_escape($runid) . '%']),
            'course' => pqfwc_count_sql("SELECT COUNT(1) FROM {local_prequran_course_audit} WHERE workspaceid = :workspaceid AND " . $GLOBALS['DB']->sql_like('details', ':runid'), ['workspaceid' => $workspaceid, 'runid' => '%' . $GLOBALS['DB']->sql_like_escape($runid) . '%']),
            'finance' => pqfwc_count_sql("SELECT COUNT(1) FROM {local_prequran_finance_audit} WHERE workspaceid = :workspaceid AND " . $GLOBALS['DB']->sql_like('details', ':runid'), ['workspaceid' => $workspaceid, 'runid' => '%' . $GLOBALS['DB']->sql_like_escape($runid) . '%']),
        ],
    ];
}

if ($action === 'create_verify') {
    try {
        if ($runid === '') {
            throw new invalid_parameter_exception('runid is required.');
        }
        $result = pqfwc_create_verify($consumerid, $workspaceid, $runid, $coursekey, $invoiceamount, $partialamount);
    } catch (Throwable $e) {
        $error = $e->getMessage();
    }
}

