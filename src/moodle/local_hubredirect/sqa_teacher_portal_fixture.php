function pqsqtf_table_ready(string $table): bool {
    global $DB;
    try {
        return $DB->get_manager()->table_exists($table);
    } catch (Throwable $e) {
        return false;
    }
}

function pqsqtf_has_field(string $table, string $field): bool {
    global $DB;
    try {
        return $DB->get_manager()->field_exists($table, $field);
    } catch (Throwable $e) {
        return false;
    }
}

function pqsqtf_filter_record(string $table, array $record): stdClass {
    $filtered = [];
    foreach ($record as $field => $value) {
        if ($field === 'id' || pqsqtf_has_field($table, $field)) {
            $filtered[$field] = $value;
        }
    }
    return (object)$filtered;
}

function pqsqtf_token(string $value): string {
    $token = strtolower(preg_replace('/[^a-zA-Z0-9]+/', '', $value));
    return substr($token !== '' ? $token : sha1((string)time()), -18);
}

function pqsqtf_upsert_workspace_member(int $workspaceid, int $userid, string $role): int {
    global $DB, $USER;
    if ($workspaceid <= 0 || $userid <= 0 || !pqsqtf_table_ready('local_prequran_workspace_member')) {
        return 0;
    }
    $now = time();
    $existing = $DB->get_record('local_prequran_workspace_member', [
        'workspaceid' => $workspaceid,
        'userid' => $userid,
    ], '*', IGNORE_MISSING);
    $record = [
        'workspaceid' => $workspaceid,
        'userid' => $userid,
        'role' => $role,
        'status' => 'active',
        'notes' => 'Created or refreshed by SQA teacher portal fixture.',
        'createdby' => (int)$USER->id,
        'timecreated' => (int)($existing->timecreated ?? $now),
        'timemodified' => $now,
    ];
    if ($existing) {
        $record['id'] = (int)$existing->id;
        $DB->update_record('local_prequran_workspace_member', pqsqtf_filter_record('local_prequran_workspace_member', $record));
        return (int)$existing->id;
    }
    return (int)$DB->insert_record('local_prequran_workspace_member', pqsqtf_filter_record('local_prequran_workspace_member', $record));
}

function pqsqtf_create_student(int $workspaceid, string $runid, string $password): array {
