function pqfwc_table_ready(string $table): bool {
    global $DB;
    try {
        return $DB->get_manager()->table_exists($table);
    } catch (Throwable $e) {
        return false;
    }
}

function pqfwc_has_field(string $table, string $field): bool {
    global $DB;
    try {
        return $DB->get_manager()->field_exists($table, $field);
    } catch (Throwable $e) {
        return false;
    }
}

function pqfwc_filter_record(string $table, array $record): stdClass {
    $filtered = [];
    foreach ($record as $field => $value) {
        if ($field === 'id' || pqfwc_has_field($table, $field)) {
            $filtered[$field] = $value;
        }
    }
    return (object)$filtered;
}

function pqfwc_token(string $runid): string {
    $token = strtolower(preg_replace('/[^a-zA-Z0-9]+/', '', $runid));
    return substr($token !== '' ? $token : sha1((string)time()), -18);
}

function pqfwc_money(string $value, string $fallback = '0.00'): string {
    $clean = preg_replace('/[^0-9.\\-]+/', '', $value);
    if ($clean === '' || !is_numeric($clean)) {
        $clean = $fallback;
    }
    return number_format(max(0, (float)$clean), 2, '.', '');
}

function pqfwc_count_sql(string $sql, array $params = []): int {
    global $DB;
    try {
        return (int)$DB->count_records_sql($sql, $params);
    } catch (Throwable $e) {
        return 0;
    }
}

function pqfwc_insert_safe(string $table, array $record): int {
    global $DB;
    if (!pqfwc_table_ready($table)) {
        return 0;
    }
    try {
        return (int)$DB->insert_record($table, pqfwc_filter_record($table, $record));
    } catch (Throwable $e) {
        return 0;
    }
}

