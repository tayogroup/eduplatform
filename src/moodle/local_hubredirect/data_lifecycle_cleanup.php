    return [
        ['category' => 'generated people lifecycle', 'status' => ((int)$active['active_users'] === 0 && (int)$archived['archived_users'] >= 3) ? 'PASS' : 'CHECK', 'evidence' => 'active_users=' . $active['active_users'] . '; archived_users=' . $archived['archived_users']],
        ['category' => 'invoice lifecycle', 'status' => ((int)$active['active_invoice_rows'] === 0 && (int)$counts['invoices_archived'] > 0) ? 'PASS' : 'CHECK', 'evidence' => 'active_invoice_rows=' . $active['active_invoice_rows'] . '; invoices_archived=' . $counts['invoices_archived']],
        ['category' => 'course offering lifecycle', 'status' => ((int)$active['active_course_offerings'] === 0 && (int)$counts['course_offerings_archived'] > 0) ? 'PASS' : 'CHECK', 'evidence' => 'active_course_offerings=' . $active['active_course_offerings'] . '; offerings_archived=' . $counts['course_offerings_archived']],
        ['category' => 'materials lifecycle', 'status' => ((int)$active['active_materials'] === 0 && (int)$active['active_assignments'] === 0 && (int)$counts['materials_archived'] > 0) ? 'PASS' : 'CHECK', 'evidence' => 'active_materials=' . $active['active_materials'] . '; active_assignments=' . $active['active_assignments'] . '; materials_archived=' . $counts['materials_archived']],
        ['category' => 'active queues hidden', 'status' => $activeafter === 0 ? 'PASS' : 'CHECK', 'evidence' => 'active_queue_total=' . $activeafter . '; parent_visible_links=' . $active['parent_visible_links']],
        ['category' => 'audit reporting retained', 'status' => $auditretained > 0 ? 'PASS' : 'CHECK', 'evidence' => 'finance_audit=' . $archived['retained_finance_audit'] . '; course_audit=' . $archived['retained_course_audit'] . '; live_audit=' . $archived['retained_live_audit']],
        ['category' => 'delete policy', 'status' => $cleanupmode === 'delete' ? 'BLOCKED' : 'PASS', 'evidence' => $cleanupmode === 'delete' ? 'delete blocked for audit-retained SQA lifecycle records' : 'archive mode completed'],
    ];
}

