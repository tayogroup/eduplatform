    $record = [
        'workspaceid' => $workspaceid,
        'userid' => $userid,
        'role' => $role,
        'workspace_role' => $role,
        'status' => 'active',
        'notes' => 'SQA lifecycle fixture active before archive.',
        'createdby' => (int)$USER->id,
