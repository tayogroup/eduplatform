    }
    return (int)$DB->insert_record('local_prequran_workspace_member', pqdlc_filter_record('local_prequran_workspace_member', $record));
}

function pqdlc_upsert_simple(string $table, array $conditions, array $record): int {
    global $DB;
    if (!pqdlc_table_ready($table)) {
        return 0;
    }
    $existing = $DB->get_record($table, $conditions, '*', IGNORE_MISSING);
    if ($existing) {
        $record['id'] = (int)$existing->id;
        $DB->update_record($table, pqdlc_filter_record($table, $record));
        return (int)$existing->id;
    }
    return (int)$DB->insert_record($table, pqdlc_filter_record($table, $record));
}

