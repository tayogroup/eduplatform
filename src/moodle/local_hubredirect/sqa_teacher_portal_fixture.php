        $existingprofile = $DB->get_record('local_prequran_student_profile', ['userid' => $userid], '*', IGNORE_MISSING);
        $record = [
            'userid' => $userid,
            'workspaceid' => $workspaceid,
            'displayname' => 'Portal Student ' . substr($token, -6),
            'student_display_name' => 'Portal Student ' . substr($token, -6),
            'status' => 'active',
            'current_level' => 'Pre-quraan Course',
            'course_type' => 'pre_quraan',
            'createdby' => (int)$USER->id,
            'timecreated' => (int)($existingprofile->timecreated ?? time()),
            'timemodified' => time(),
        ];
