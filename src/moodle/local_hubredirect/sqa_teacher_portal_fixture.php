    ], '*', IGNORE_MISSING);

    if ($existing) {
        $userid = (int)$existing->id;
    } else {
        $userid = (int)user_create_user((object)[
            'auth' => 'manual',
            'confirmed' => 1,
            'mnethostid' => $CFG->mnet_localhost_id,
            'username' => $username,
            'password' => $password,
            'firstname' => 'Portal',
            'lastname' => 'Student ' . substr($token, -6),
            'email' => $email,
            'emailstop' => 1,
