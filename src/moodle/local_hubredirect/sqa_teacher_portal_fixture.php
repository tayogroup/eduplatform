            $record['id'] = (int)$existingprofile->id;
            $DB->update_record('local_prequran_student_profile', pqsqtf_filter_record('local_prequran_student_profile', $record));
        } else {
            $DB->insert_record('local_prequran_student_profile', pqsqtf_filter_record('local_prequran_student_profile', $record));
        }
    }

    return [
