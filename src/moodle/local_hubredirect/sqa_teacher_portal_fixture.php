}

function pqsqtf_reset_teacher_password(int $teacherid, string $password): void {
    global $DB;
    $teacher = core_user::get_user($teacherid, '*', MUST_EXIST);
    update_internal_user_password($teacher, $password);
    $DB->update_record('user', (object)[
        'id' => $teacherid,
        'auth' => 'manual',
        'confirmed' => 1,
        'suspended' => 0,
        'deleted' => 0,
        'policyagreed' => 1,
        'emailstop' => (int)($teacher->emailstop ?? 0),
        'timemodified' => time(),
    ]);
    if (function_exists('unset_user_preference')) {
        unset_user_preference('auth_forcepasswordchange', $teacherid);
    }
}

